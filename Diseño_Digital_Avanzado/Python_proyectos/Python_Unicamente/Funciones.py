#Funciones Matemáticas
#Para usar funciones Matematicas debe importar la libreria
import math

pi=math.pi
#Funciones matemáticas
print(math.sin(pi/3))
print(math.cos(pi/3))
print(math.tan(pi/3))
#Funciones Inversas
print(math.asin(0.5))
print(math.acos(0.5))
print(math.atan(0.5))
#Para covertir de radianes a grados
print(math.degrees(0.86))
#Para Convertir de Grados a radianes
print(math.radians(80))
#La hipotenusa de un triangulo
a=1
b=2
print(math.hypot(a,b))
